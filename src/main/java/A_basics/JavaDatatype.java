package A_basics;


public class JavaVariables {
    public static void main(String[] args) {
        int num1=10;// 4 byte
        long num2=1000000; //8 byte
        float num3=10.3f; //
        double num4=10.33333;
        char ch ='c';
        boolean bool = true;
        byte a= 1;
        String str= "Rahul";
        System.out.println("int is :"+num1);
        System.out.println("long is :"+num2);
        System.out.println("float is :"+num3);
        System.out.println("double is :"+num4);
        System.out.println("char is :"+ch);
        System.out.println("boolean is :"+bool);
        System.out.println("byte is :"+a);
        System.out.println("string is :"+str);

    }
}
