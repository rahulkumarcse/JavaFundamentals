package B_controls;

public class SwitchStatement {
    public static void main(String[] args) {
        int a =5;
        switch (a){
            case 1 :             System.out.println("one");
            case 2: System.out.println("Two");
            case 5: System.out.println("Five");
            break;
            default : System.out.println("None matching");

        }
    }
}
